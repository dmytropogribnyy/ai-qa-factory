from __future__ import annotations

from core.config import get_settings
from core.orchestrator import QAFactoryOrchestrator
from tools.code_executor import SafeCodeExecutor


def test_job_mode_generates_outputs(tmp_path, monkeypatch):
    monkeypatch.setenv("OUTPUT_DIR", str(tmp_path / "outputs"))
    settings = get_settings()
    state = QAFactoryOrchestrator(settings).run("job", "Need Playwright TypeScript QA automation for SaaS CI/CD.")
    assert state.stack_choice == "playwright-ts"
    assert "proposal.md" in state.generated_outputs
    assert "qa_plan.md" in state.generated_outputs
    assert "HUMAN_REVIEW_REQUIRED.md" in state.generated_outputs
    assert (tmp_path / "outputs" / state.project_id / "proposal.md").exists()


def test_scaffold_mode_generates_playwright_framework(tmp_path, monkeypatch):
    monkeypatch.setenv("OUTPUT_DIR", str(tmp_path / "outputs"))
    settings = get_settings()
    state = QAFactoryOrchestrator(settings).run("scaffold", "AI SaaS MVP with login, dashboard, API and GitHub Actions.")
    assert "framework/package.json" in state.generated_outputs
    assert "framework/playwright.config.ts" in state.generated_outputs
    assert "framework/pages/LoginPage.ts" in state.generated_outputs
    assert "framework/tests/ui/smoke.spec.ts" in state.generated_outputs
    assert "framework/tests/a11y/basic-a11y.spec.ts" in state.generated_outputs
    assert "TEST_RUNNER_NOTE.md" in state.generated_outputs
    assert (tmp_path / "outputs" / state.project_id / "framework" / "package.json").exists()


