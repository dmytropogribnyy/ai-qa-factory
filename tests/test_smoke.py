def test_safe_executor_blocks_non_allowlisted_command(tmp_path):
    result = SafeCodeExecutor().run(["rm", "-rf", "/"], cwd=tmp_path)
    assert result.success is False
    assert result.returncode == 126


def test_review_mode_bypasses_job_analysis(tmp_path, monkeypatch):
    monkeypatch.setenv("OUTPUT_DIR", str(tmp_path / "outputs"))
    settings = get_settings()
    state = QAFactoryOrchestrator(settings).run("review", "await page.waitForTimeout(5000);")
    assert "flakiness_review.md" in state.generated_outputs
    assert "analysis.md" not in state.generated_outputs


def test_generated_scaffold_uses_v508_and_npm_install(tmp_path, monkeypatch):
    monkeypatch.setenv("OUTPUT_DIR", str(tmp_path / "outputs"))
    settings = get_settings()
    state = QAFactoryOrchestrator(settings).run("scaffold", "Playwright TypeScript SaaS app")
    assert "Generated by AI QA Factory v5.0.8 Model Routing Profiles" in state.generated_outputs["framework/README.md"]
    workflow = state.generated_outputs["framework/.github/workflows/playwright.yml"]
    assert "npm install" in workflow
    assert "npm ci" not in workflow
