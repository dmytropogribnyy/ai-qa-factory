"""FrameworkScaffoldGenerator — builds a local Playwright TypeScript scaffold.

Phase 3A — scaffold generation only:
- No URL fetching.
- No browser execution.
- No Playwright execution.
- No npm/npx execution.
- No TypeScript compilation.
- No credential use.
- No external calls.
- No test execution.
- Generated scaffold requires human review before any local validation.
"""
from __future__ import annotations

import json
import re
from pathlib import Path
from typing import List, Optional

from core.schemas.framework_scaffold import FrameworkFile, FrameworkScaffold, FrameworkScaffoldPlan
from core.schemas.project_blueprint import ProjectBlueprint
from core.schemas.qa_strategy import QAStrategy


# ---------------------------------------------------------------------------
# FrameworkScaffoldGenerator
# ---------------------------------------------------------------------------

class FrameworkScaffoldGenerator:
    """Generates a local Playwright TypeScript scaffold from blueprint + strategy."""

    # ------------------------------------------------------------------
    # Public API
    # ------------------------------------------------------------------

    def build_scaffold_plan(
        self,
        blueprint: ProjectBlueprint,
        strategy: Optional[QAStrategy] = None,
    ) -> FrameworkScaffoldPlan:
        fw_type = self._decide_framework_type(blueprint, strategy)
        included, deferred, blocked = self._categorise_layers(blueprint, strategy)
        structure = self._recommended_structure(blueprint, strategy)
        approvals = list(blueprint.required_approvals) if blueprint.required_approvals else [
            "Target URL approval required before any test execution.",
            "Test credential approval required before auth testing.",
        ]
        return FrameworkScaffoldPlan(
            project_id=blueprint.project_id,
            target_framework=fw_type,
            recommended_structure=structure,
            included_layers=included,
            deferred_layers=deferred,
            blocked_layers=blocked,
            required_approvals=approvals,
            notes=[
                "Scaffold generation is local file generation only.",
                "No tests are executed in Phase 3A.",
                "Run docs/SCAFFOLD_REVIEW_CHECKLIST.md before any local validation.",
            ],
        )

    def generate_scaffold(
        self,
        blueprint: ProjectBlueprint,
        strategy: Optional[QAStrategy] = None,
        out_dir: Optional[Path] = None,
    ) -> FrameworkScaffold:
        if out_dir is None:
            out_dir = Path("outputs") / blueprint.project_id / "03_framework" / "playwright"
        out_dir = Path(out_dir)

        fw_type = self._decide_framework_type(blueprint, strategy)
        proj_name = self._sanitize_project_name(
            blueprint.project_name or blueprint.project_id
        )
        files: List[FrameworkFile] = []

        manifest = self._get_file_manifest(blueprint, strategy)
        for entry in manifest:
            rel_path = entry["path"]
            ftype = entry["file_type"]
            purpose = entry["purpose"]
            content = self._generate_file_content(rel_path, blueprint, strategy, proj_name)
            dest = out_dir / rel_path
            dest.parent.mkdir(parents=True, exist_ok=True)
            dest.write_text(content, encoding="utf-8")
            files.append(FrameworkFile(
                id=f"ff-{len(files)+1:03d}",
                path="03_framework/playwright/" + rel_path.replace("\\", "/"),
                purpose=purpose,
                file_type=ftype,
                client_visible=False,
                generated=True,
                requires_review=True,
            ))

        generated_from = ["ProjectBlueprint"]
        if strategy is not None:
            generated_from.append("QAStrategy")

        scaffold = FrameworkScaffold(
            project_id=blueprint.project_id,
            framework_type=fw_type,
            language="typescript",
            test_runner="playwright",
            root_path=str(out_dir),
            files=files,
            generated_from=generated_from,
            scaffold_status="generated",
            client_visible=False,
            requires_review=True,
            execution_allowed=False,
            notes=[
                "Scaffold generated by FrameworkScaffoldGenerator (Phase 3A).",
                "No tests have been executed.",
                "No URLs have been fetched.",
                "No credentials have been used.",
                "Review SCAFFOLD_REVIEW_CHECKLIST.md before any local validation.",
                "execution_allowed=False — explicit approval required before running.",
            ],
        )

        # Write FRAMEWORK_SCAFFOLD.json + .md into the root
        self._write_scaffold_metadata(scaffold, out_dir)
        return scaffold

    def render_scaffold_artifacts(
        self,
        scaffold: FrameworkScaffold,
        out_dir: Path,
    ) -> dict:
        """Write FRAMEWORK_SCAFFOLD.json and .md; return {name: path} dict."""
        out_dir = Path(out_dir)
        out_dir.mkdir(parents=True, exist_ok=True)
        self._write_scaffold_metadata(scaffold, out_dir)
        json_path = out_dir / "FRAMEWORK_SCAFFOLD.json"
        md_path = out_dir / "FRAMEWORK_SCAFFOLD.md"
        return {
            "framework_scaffold_json": str(json_path),
            "framework_scaffold_md": str(md_path),
        }

    # ------------------------------------------------------------------
    # Internal helpers
    # ------------------------------------------------------------------

    def _sanitize_project_name(self, name: str) -> str:
        name = re.sub(r"[^\w\s-]", "", name.lower())
        name = re.sub(r"[\s_]+", "-", name.strip())
        name = re.sub(r"-+", "-", name)
        return name[:50] or "qa-project"

    def _decide_framework_type(
        self, blueprint: ProjectBlueprint, strategy: Optional[QAStrategy]
    ) -> str:
        pt = blueprint.project_type
        if pt == "api_backend":
            return "api_only"
        if pt == "mixed_ui_api":
            return "mixed_ui_api"
        return "playwright_ts"

    def _has_layer(self, strategy: Optional[QAStrategy], layer_name: str) -> bool:
        if strategy is None:
            return False
        for tl in strategy.test_layers:
            if tl.layer.lower() == layer_name.lower() and tl.recommended and not tl.blocked:
                return True
        return False

    def _has_any_signal(self, blueprint: ProjectBlueprint, *signals: str) -> bool:
        text = " ".join([
            blueprint.project_type or "",
            blueprint.client_goal or "",
            blueprint.scope_notes or "",
            " ".join(blueprint.risk_areas),
            " ".join(blueprint.application_surfaces),
        ]).lower()
        return any(s.lower() in text for s in signals)

    def _categorise_layers(self, blueprint, strategy):
        included, deferred, blocked = [], [], []
        if strategy is None:
            return ["smoke", "ui", "manual_exploratory"], ["api", "visual", "performance"], ["mobile_native"]
        for tl in strategy.test_layers:
            if tl.blocked:
                blocked.append(tl.layer)
            elif tl.recommended:
                included.append(tl.layer)
            else:
                deferred.append(tl.layer)
        return included, deferred, blocked

