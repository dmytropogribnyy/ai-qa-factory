    def _generate_file_content(
        self,
        rel_path: str,
        blueprint: ProjectBlueprint,
        strategy: Optional[QAStrategy],
        proj_name: str,
    ) -> str:
        dispatch = {
            "package.json":           lambda: self._gen_package_json(proj_name),
            "tsconfig.json":          lambda: self._gen_tsconfig(),
            "playwright.config.ts":   lambda: self._gen_playwright_config(blueprint, strategy),
            ".gitignore":             lambda: self._gen_gitignore(),
            ".env.example":           lambda: self._gen_env_example(blueprint),
            "README.md":              lambda: self._gen_readme(blueprint, strategy, proj_name),
            "tests/smoke/smoke.spec.ts":           lambda: self._gen_smoke_spec(blueprint),
            "tests/regression/regression-placeholder.spec.ts": lambda: self._gen_regression_spec(blueprint, strategy),
            "tests/auth/auth-placeholder.spec.ts": lambda: self._gen_auth_spec(blueprint),
            "tests/api/api-placeholder.spec.ts":   lambda: self._gen_api_spec(blueprint),
            "tests/ecommerce/checkout-placeholder.spec.ts": lambda: self._gen_checkout_spec(),
            "tests/admin/admin-placeholder.spec.ts": lambda: self._gen_admin_spec(),
            "pages/BasePage.ts":       lambda: self._gen_base_page(),
            "pages/LoginPage.ts":      lambda: self._gen_login_page(),
            "pages/DashboardPage.ts":  lambda: self._gen_dashboard_page(),
            "fixtures/test-fixtures.ts": lambda: self._gen_fixtures(blueprint),
            "utils/env.ts":            lambda: self._gen_env_util(),
            "utils/test-data.ts":      lambda: self._gen_test_data_util(blueprint),
            "utils/api-client.ts":     lambda: self._gen_api_client(),
            "test-data/README.md":     lambda: self._gen_test_data_readme(),
            "test-data/sample-users.example.json": lambda: self._gen_sample_users(),
            "docs/TEST_STRATEGY.md":   lambda: self._gen_test_strategy_doc(blueprint, strategy),
            "docs/HOW_TO_RUN.md":      lambda: self._gen_how_to_run(blueprint),
            "docs/SCAFFOLD_REVIEW_CHECKLIST.md": lambda: self._gen_review_checklist(blueprint, strategy),
        }
        fn = dispatch.get(rel_path)
        if fn:
            return fn()
        return f"// Generated placeholder for {rel_path}\n// Review before use.\n"

